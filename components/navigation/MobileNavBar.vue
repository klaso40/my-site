<script>
import gsap from 'gsap'
export default {
  data() {
    return {
      expanded: false,
    }
  },
  watch: {
    expanded(newValue, oldValue) {
      if (newValue) {
        this.expandTabBar()
      } else {
        this.closeTabBar()
      }
    },
    $route(to, from) {
      this.$nextTick(function () {
        const element = document.querySelectorAll('.nuxt-link-exact-active')[1]
        this.mobileSliderAnimation(element.offsetLeft, element.offsetWidth)
      })
    },
  },
  methods: {
    expandTabBar() {
      this.expanded = true
      this.hamburgerAnimation()
      gsap.to('#tab-bar', {
        width: '90%',
        duration: 0.2,
        onComplete: () => {
          const element = document.querySelectorAll(
            '.nuxt-link-exact-active'
          )[1]
          this.mobileSliderAnimation(element.offsetLeft, element.offsetWidth)
        },
      })
    },
    hamburgerAnimation() {
      const tl = gsap.timeline({})
      if (this.expanded) {
        tl.to('#hamburger-stick1', 0.3, {
          transform: 'rotate(45deg)',
          position: 'absolute',
        })
        tl.to(
          '#hamburger-stick2',
          0.3,
          { transform: 'rotate(-45deg)', position: 'absolute' },
          '-=0.3'
        )
      } else {
        tl.to('#hamburger-stick1', 0.3, {
          transform: 'rotate(0deg)',
          position: 'static',
        })
        tl.to(
          '#hamburger-stick2',
          0.3,
          { transform: 'rotate(0deg)', position: 'static' },
          '-=0.3'
        )
      }
    },
    closeTabBar() {
      this.expanded = false
      this.hamburgerAnimation()
      gsap.to('#tab-bar', {
        width: '3.5rem',
        duration: 0.2,
      })
    },
    mobileSliderAnimation(leftValue, width) {
      gsap.to('#mobile-indicator', {
        left: leftValue + width / 2 - 3,
        duration: 0.2,
        ease: 'linear',
      })
    },
  },
}
